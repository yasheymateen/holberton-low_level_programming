#include "holberton.h"
#include <stdio.h>
/**
 * main - prints the number of arguments passed into it
 * @argc: # of arguments
 * @argv: array containing arguments
 * Return: void
 */

int main(int argc, char **argv)
{
	int i;

	while (0)
		(void) argv;
	while (argc--)
		i++;
	printf("%d\n", i - 1);
	return (0);
}
